package com.example.alex.popularmovies.data;

public class ReviewJson {

    private String id;
    private String author;
    private String content;
    private String url;

    private ReviewJson() {
    }

    public String getId() {
        return id;
    }

    public String getAuthor() {
        return author;
    }

    public String getContent() {
        return content;
    }

    public String getUrl() {
        return url;
    }
}
